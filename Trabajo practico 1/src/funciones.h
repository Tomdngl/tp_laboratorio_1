#ifndef FUNCIONES_H_
#define FUNCIONES_H_
/**
 * @fn int calcularFactorial(int)
 * @brief Esta función nos permite calcular el factorial de un número entero.
 *
 * @param En este único parámetro vamos a mandar el número al cual queremos calcular
 * @return La función nos devuelve el resultado del factorial calculado.
 */
int calcularFactorial(float);
/**
 * @fn int calcularSuma(int, int)
 * @brief Esta función nos permite sumar dos números.
 *
 * @param El primer parámetro corresponde al operando A
 * @param El segundo parámetro corresponde al operando B
 * @return La funcion devuelve el resultado de la suma.
 */
float calcularSuma(float, float);
/**
 * @fn int calcularResta(float, float)
 * @brief Esta función nos permite restar dos números.
 *
 * @param El primer parámetro corresponde al operando A
 * @param El segundo parámetro corresponde al operando B
 * @return La funcion devuelve el resultado de la resta.
 */
float calcularResta(float, float);
/**
 * @fn float calcularMultiplicacion(float, float)
 * @brief Esta función nos permite multiplicar el primer operando por el segundo.
 *
 * @param El primer parámetro corresponde al operando A
 * @param El segundo parámetro corresponde al operando B
 * @return La funcion devuelve el resultado de la multiplicacion.
 */
float calcularMultiplicacion(float, float);
/**
 * @fn float calcularDivision(float, float)
 * @brief Esta función nos permite dividir el primer operando por el segundo.
 *
 * @param El primer parámetro corresponde al operando A
 * @param El segundo parámetro corresponde al operando B
 * @return La funcion devuelve el resultado de la división.
 */
float calcularDivision(float, float);

#endif /* FUNCIONES_H_ */
